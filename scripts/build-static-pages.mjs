#!/usr/bin/env node

import { writeFileSync } from "node:fs";
import { join } from "node:path";

import {
    escapeAttribute,
    escapeHtml,
    renderAdSlot,
    renderBreadcrumb,
    renderDocument,
    renderExerciseLibrary,
    renderStaticFooter,
    renderStaticHeader
} from "./site-template.mjs";
import { ROOT, loadCatalog, loadDiscovery, loadPages } from "./source-data.mjs";

const catalog = loadCatalog();
const discovery = loadDiscovery();
const pages = loadPages();

let generatedPages = 0;

for (const page of pages) {
    let html = "";

    if (page.kind === "home") {
        html = renderHomePage(page, catalog);
    } else if (page.kind === "content") {
        html = renderContentPage(page);
    } else {
        throw new Error(`Unsupported page kind "${page.kind}" in ${page.file}`);
    }

    writeFileSync(join(ROOT, page.file), html);
    generatedPages += 1;
}

console.log(`Generated ${generatedPages} static pages from src/pages/.`);

function renderHomePage(page, catalogData) {
    const body = `${renderStaticHeader({ pageType: "home" })}

    <hr class="top-divider">
    <main class="page-main">
${renderExerciseLibrary(catalogData, {
        unit: page.unit || "kg",
        titleTag: "h1",
        titleText: page.heading
    })}
${renderAdSlot()}
    </main>

${renderStaticFooter(page.file)}

    <script src="app.js"></script>
`;

    return renderDocument({
        title: page.title,
        stylesheets: page.stylesheets || ["styles.css"],
        body,
        generatedComment: "<!-- Generated by scripts/build-static-pages.mjs. Edit src/pages/*.json and src/catalog.json instead of editing this file directly. -->"
    });
}

function renderContentPage(page) {
    const body = `${renderStaticHeader({ pageType: "content" })}

    <hr class="top-divider">
    <main class="page-main">
${renderBreadcrumb([
        { label: "Home", href: "index.html" },
        { label: page.heading }
    ])}

        <section class="container content-shell ${escapeAttribute(page.shellClass || "")}">
            <div class="content-intro">
                <h1>${escapeHtml(page.heading)}</h1>
${(page.intro || []).map((paragraph) => `                <p>${escapeHtml(paragraph)}</p>`).join("\n")}
            </div>

${(page.sections || []).map((section) => renderContentSection(section)).join("\n\n")}
        </section>
    </main>

${renderStaticFooter(page.file)}

    <script src="app.js"></script>
`;

    return renderDocument({
        title: page.title,
        stylesheets: page.stylesheets || ["styles.css"],
        body,
        generatedComment: "<!-- Generated by scripts/build-static-pages.mjs. Edit src/pages/*.json instead of editing this file directly. -->"
    });
}

function renderContentSection(section) {
    const blocks = [
        ...(section.paragraphs || []).map((text) => ({ type: "paragraph", text })),
        ...(section.blocks || [])
    ];

    return `            <section class="${escapeAttribute(section.className || "content-card")}">
${section.heading ? `                <h2>${escapeHtml(section.heading)}</h2>\n` : ""}${blocks.map((block) => renderContentBlock(block)).join("\n")}
            </section>`;
}

function renderContentBlock(block) {
    if (block.type === "paragraph") {
        return `                <p>${escapeHtml(block.text)}</p>`;
    }

    if (block.type === "subheading") {
        return `                <h3>${escapeHtml(block.text)}</h3>`;
    }

    if (block.type === "list") {
        return `                <ul>
${block.items.map((item) => `                    <li>${escapeHtml(item)}</li>`).join("\n")}
                </ul>`;
    }

    if (block.type === "email") {
        const mailto = block.mailto || block.value;
        return `                <p class="${escapeAttribute(block.className || "email")}">
                    ${escapeHtml(block.label || "Email")}: <a href="mailto:${escapeAttribute(mailto)}">${escapeHtml(block.value)}</a>
                </p>`;
    }

    throw new Error(`Unsupported content block type "${block.type}"`);
}
